    private void excluirOperacao() {
        int linha = tbOperacaoPesquisar.getSelectedRow();
        String nome = (String) tbOperacaoPesquisar.getValueAt(linha, 3);
        int codigo = (Integer) tbOperacaoPesquisar.getValueAt(linha, 0);
        int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja"
                + " excluir o registro \nNome: "
                + nome + " ?", "Atenção", JOptionPane.YES_OPTION);

        if (opcao == JOptionPane.OK_OPTION) {
            if (controllerOperacao.excluirOperacaoAcController(codigo)) {
                JOptionPane.showMessageDialog(this, "Registro excluido com sucesso!");
                carregaOperacao();
                carregaOperacaoConsulta();
                if (solicitante != null) {
                    if (solicitante instanceof ViewEquipamentoAc) {
                        ViewEquipamentoAc vp = (ViewEquipamentoAc) solicitante;
                        vp.addItemLista(txtDescOp.getText());
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private boolean salvarOperacao() {
        modelOperacao.setOrdem(Integer.parseInt(this.txtOrdem.getText()));
        modelOperacao.setCod_op(Integer.parseInt(this.txtCodOp.getText()));
        modelOperacao.setOperacao(this.txtDescOp.getText());
        modelOperacao.setCod_simbol_op(this.cbClassificacao.getSelectedItem().toString());
        modelOperacao.setOpe_setor(this.cbSetor.getSelectedItem().toString());
        modelOperacao.setFornecedor(this.txtFornecedor.getText());
        modelOperacao.setPreparado_por(this.txtPreparadoPor.getText());
        modelOperacao.setTelefone(this.txtTelefone.getText());
        modelOperacao.setResponsavel_processo(this.txtResponsavel.getText());
        modelOperacao.setEquipe(this.txtEquipe.getText());
        modelOperacao.setCliente(this.txtCliente.getText());
        try {
            modelOperacao.setData_criacao(new SimpleDateFormat("yyyy/MM/dd").format(jdtData.getDate()));

        } catch (Exception ex) {
            Logger.getLogger(ViewOperacaoAc.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (controllerOperacao.salvarOperacaoAcController(modelOperacao) > 0) {
            JOptionPane.showMessageDialog(this, "Registro gravado com sucesso!");
            this.desabilitarCampos();
            this.carregaOperacao();
            this.carregaOperacaoConsulta();
            if (solicitante != null) {
                if (solicitante instanceof ViewEquipamentoAc) {
                    ViewEquipamentoAc vp = (ViewEquipamentoAc) solicitante;
                    vp.addItemLista(txtDescOp.getText());
                }
            }
            limparDados();
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao tentar gravar os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private boolean recuperarOperacao() {
        int linha = this.tbOperacaoPesquisar.getSelectedRow();
        int codigo = (Integer) tbOperacaoPesquisar.getValueAt(linha, 0);
        try {
            modelOperacao = controllerOperacao.getOperacaoAcController(codigo);
            this.txtId.setText(String.valueOf(modelOperacao.getId_operacao()));
            this.txtOrdem.setText(String.valueOf(modelOperacao.getOrdem()));
            this.txtCodOp.setText(String.valueOf(modelOperacao.getCod_op()));
            this.txtDescOp.setText(modelOperacao.getOperacao());
            this.cbClassificacao.setSelectedItem(modelOperacao.getCod_simbol_op());
            this.cbSetor.setSelectedItem(modelOperacao.getOpe_setor());
            this.txtFornecedor.setText(modelOperacao.getFornecedor());
            this.txtPreparadoPor.setText(modelOperacao.getPreparado_por());
            this.txtTelefone.setText(modelOperacao.getTelefone());
            this.txtResponsavel.setText(modelOperacao.getResponsavel_processo());
            this.txtEquipe.setText(modelOperacao.getEquipe());
            this.txtCliente.setText(modelOperacao.getCliente());
            this.jdtData.setDate(new SimpleDateFormat("yyyy/MM/dd").parse(modelOperacao.getData_criacao()));
            return true;
        } catch (Exception e) {
//            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }

