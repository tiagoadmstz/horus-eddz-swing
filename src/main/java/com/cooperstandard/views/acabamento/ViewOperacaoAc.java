    private void carregaOperacao() {
        listaModelOperacao = controllerOperacao.getListaOperacaoAcController();
        DefaultTableModel modelo = (DefaultTableModel) tbOperacaoPesquisar.getModel();
        modelo.setNumRows(0);
        int cont = listaModelOperacao.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelOperacao.get(i).getId_operacao(),
                listaModelOperacao.get(i).getOrdem(),
                listaModelOperacao.get(i).getCod_op(),
                listaModelOperacao.get(i).getOperacao(),
                listaModelOperacao.get(i).getCod_simbol_op(),
                listaModelOperacao.get(i).getFornecedor(),
                listaModelOperacao.get(i).getPreparado_por(),
                listaModelOperacao.get(i).getTelefone(),
                listaModelOperacao.get(i).getResponsavel_processo(),
                listaModelOperacao.get(i).getEquipe(),
                listaModelOperacao.get(i).getData_criacao()

            });

        }

    }

