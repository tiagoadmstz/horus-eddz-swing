    private void CarregaTabela() {
        listaModelIdControle = controllerIdControle.getListaIdControleController();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        int cont = listaModelIdControle.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelIdControle.get(i).getID(),
                listaModelIdControle.get(i).getLinha(),
                listaModelIdControle.get(i).getMaterial(),
                new SimpleDateFormat("dd/MM/yyyy hh:mm").format(listaModelIdControle.get(i).getData_base()),
                listaModelIdControle.get(i).getScrap_total(),
                listaModelIdControle.get(i).getEvidencia()

            });

        }

    }

    public void ImprimiImagem() {
        ImageIcon img = new ImageIcon(jTextField1.getText());
        jLabel9.setIcon(img);
        ImageIcon image = new ImageIcon(jTextField1.getText());
        jLabel9.setIcon(new ImageIcon(image.getImage().getScaledInstance(jLabel9.getWidth(), jLabel9.getHeight(), Image.SCALE_DEFAULT)));
    }

    private boolean recuperaIdControle() {
        int linha = this.jTable1.getSelectedRow();
        int codigo = (Integer) jTable1.getValueAt(linha, 0);
        try {
            modelIdControle = controllerIdControle.getIdControleController(codigo);
            this.jTextField2.setText(String.valueOf(modelIdControle.getID()));
            this.jTextField4.setText(String.valueOf(modelIdControle.getLinha()));
            this.jTextField3.setText(String.valueOf(modelIdControle.getMaterial()));
            this.jTextField5.setText(new SimpleDateFormat("dd/MM/yyyy hh:mm").format(modelIdControle.getData_base()));
            this.jTextField6.setText(String.valueOf(modelIdControle.getScrap_total()));
            this.jTextField1.setText(String.valueOf(modelIdControle.getEvidencia()));
//            this.jdtData.setDate(new SimpleDateFormat("yyyy/MM/dd").parse(modelOperacao.getData_criacao()));
            return true;
        } catch (Exception e) {
//            JOptionPane.showMessageDialog(this, "CÃ³digo invÃ¡lido ou nenhum registro selecionado", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }

    private boolean alterarIdControle() {
        modelIdControle.setEvidencia(this.jTextField1.getText());
        if (controllerIdControle.atualizarIdControleController(modelIdControle)) {
            JOptionPane.showMessageDialog(this, "Registro alterado com sucesso!");
//            this.desabilitarCampos();
//            this.carregaOperacao();
            limparDados();
            CarregaTabela();
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

